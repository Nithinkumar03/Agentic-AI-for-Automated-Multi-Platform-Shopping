function ratingForProduct(id: string): number {
  const override = RATING_OVERRIDES[id];
  if (override !== undefined) return override;
  let h = 0;
  for (let i = 0; i < id.length; i++) h = (h * 31 + id.charCodeAt(i)) >>> 0;
  return Math.round((3.3 + ((h % 13) / 12) * 1.2) * 10) / 10;
}

function p(
  id: string,
  name: string,
  category: string,
  price: number,
  mrp: number,
  discount: string,
  unit: string,
  description: string,
): Product {
  return {
    id,
    name,
    category,
    price,
    mrp,
    discount,
    unit,
    inStock: true,
    description,
    rating: ratingForProduct(id),
  };
}

